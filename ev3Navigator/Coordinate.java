package ev3Navigator;


//Simple class that creates an object out of the given coordinates
public class Coordinate {
	
	private double X;
	private double Y;
	
	public Coordinate(double pX, double pY)
	{
		X = pX;
		Y = pY;
	}

	public double getX() {
		return X;
	}

	public double getY() {
		return Y;
	}
	
	

}
